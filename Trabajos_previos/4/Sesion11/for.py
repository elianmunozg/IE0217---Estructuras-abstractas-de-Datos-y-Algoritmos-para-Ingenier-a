# Itera sobre cada carácter en la cadena 'Python' e imprime cada uno
for x in 'Python':
    print(x)

# Utiliza range() para crear un rango de valores de 0 a 3 e itera sobre ellos
values = range(4)
for i in values:
    print(i)

# Crea una lista de lenguajes de programación y realiza una iteración
languages = ['Swift', 'Python', 'Go']
for _ in languages:
    print('Hello')  # Imprime 'Hello' por cada elemento en la lista
    print('Hi')     # Imprime 'Hi' por cada elemento en la lista

